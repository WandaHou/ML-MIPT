    def forward(self, measure, mask=False):
        if mask:
            attention_mask = self.att_msk.unsqueeze(0).expand(ceil(measure.shape[0]/self.att_msk.shape[0]),-1,-1).flatten(0,1)[:measure.shape[0]].to(device=measure.device)
            hidden_states = self.GPT(input_ids=measure, attention_mask=attention_mask, output_hidden_states=True).hidden_states[-1]
        else:
            hidden_states = self.GPT(input_ids=measure, output_hidden_states=True).hidden_states[-1]
        out = self.ffn(hidden_states) # (batch, seq_length, 32)
        out = out[:,-1,:].view(-1, 2, 16) # (batch, 32)
        A_real = out[:,0]
        A_imag = out[:,1]
        A = (A_real + 1j * A_imag).view(-1, 4, 4)
        rho = A.mT.conj() @ A
        rho /= torch.diagonal(rho, dim1=-2, dim2=-1).sum(-1).view(-1, 1, 1)
        return rho.to(torch.complex128)